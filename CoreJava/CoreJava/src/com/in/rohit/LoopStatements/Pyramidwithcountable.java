package com.in.rohit.LoopStatements;

public class Pyramidwithcountable {
   public static void main(String[] args) {
	
	   
	   
	   //1st method
	   for(int i =  1; i  <= 100000 ; i = i*10) {
	    	
	    	System.out.println(i);
	    
	      
	    }
	   
	   //2nd
	   
	   for(int a = 1 ,   j = 1;    j <= 6 ;  a = a*10, j++ ) {
		   
		    System.out.println(a);
	   
	   }
}
}
