package com.in.rohit.LoopStatements;

public class LoopCountPrint {
	
	 public static void main(String[] args) {
		//line by line pint
		
		  for(int i = 1 ; i <=10 ; i++ ){
		  
		  System.out.print(  "  " + i);
		  
		  }
		  System.out.println();
		  //reverse count print
		 for(int j = 20 ; j >=1 ; j--) {
			 System.out.print(  " " +j);
		 }
	}

}
